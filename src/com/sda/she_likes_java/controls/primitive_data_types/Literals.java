package com.sda.she_likes_java.controls.primitive_data_types;

public class Literals {
    public static void main(String[] args) {
        // literal is direct value
        // String literal is e.g. "name"
        String name = "Inga";

        //integer literal
        int age = 40; // 40is of type int

        // long literal
        long anotherAge = 30L;

        // double literal, stores much more digits
        double doubleNumber = 3.1;


        // float literal
        float floatNumber = 3.1F;

    }
}
